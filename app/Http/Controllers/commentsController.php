    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $inputs =$request->all();
        if ($request->hasFile('image')){

            $destinationPath = 'uploads'; // upload path
            $extension = $request->file('image')->getClientOriginalExtension(); // getting file extension
            $name = time() . '-' . uniqid() . '.' . $extension; // renameing file
            $request->file('image')->move($destinationPath, $name); // uploading file to given path

            $inputs['image']= '/'.$destinationPath.'/'.$name;
        }
        Comment::create($inputs);
        return redirect()->back();
    }

